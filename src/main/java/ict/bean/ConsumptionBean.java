package ict.bean;

/**
 *
 * @author HwH
 */
public class ConsumptionBean {
    String fruitId, fruitName, fruitImg, fruitCity, fruitCountry, total, fruitUnit; 

    public ConsumptionBean() {}

    public String getFruitId() {
        return fruitId;
    }

    public void setFruitId(String fruitId) {
        this.fruitId = fruitId;
    }

    public String getFruitName() {
        return fruitName;
    }

    public void setFruitName(String fruitName) {
        this.fruitName = fruitName;
    }

    public String getFruitImg() {
        return fruitImg;
    }

    public void setFruitImg(String fruitImg) {
        this.fruitImg = fruitImg;
    }

    public String getFruitCity() {
        return fruitCity;
    }

    public void setFruitCity(String fruitCity) {
        this.fruitCity = fruitCity;
    }

    public String getFruitCountry() {
        return fruitCountry;
    }

    public void setFruitCountry(String fruitCountry) {
        this.fruitCountry = fruitCountry;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }

    public String getFruitUnit() {
        return fruitUnit;
    }

    public void setFruitUnit(String fruitUnit) {
        this.fruitUnit = fruitUnit;
    }
}
